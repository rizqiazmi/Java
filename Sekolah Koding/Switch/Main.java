package com.tutorial;

public class Main {
    public static void main(String[] args) {
        
        int x = 10;
        switch (x) {
            case 1:
                System.out.println("x nya 1");
                break;

            case 10:
                System.out.println("x nya 10");
                break;

            case 5:
                System.out.println("x nya 5");
                break;
        
            default:
                System.out.println("tidak ada yang benar");
                break;
        }

    }
}